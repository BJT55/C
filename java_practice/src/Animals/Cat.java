package Animals;
class Cat implements Animal {

	public void eat() {
		System.out.println("猫咪吃小鱼干");
	}

	public void sleap() {
		System.out.println("喵咪正在睡觉");
	}

	public void think() {
		System.out.println("喵咪思考怎么抓住老鼠");
	}

	public void speak() {
		System.out.println("喵咪喵喵叫");
	}

	public void fly() {
		System.out.println("喵咪不会飞");
	}
}
