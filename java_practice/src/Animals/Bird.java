package Animals;

class Bird implements Animal {
	
	public void sleap() {
		System.out.println("小鸟在树上睡觉");
	}
	
	public void think() {
		System.out.println("小鸟思考在哪搭窝");	
	}

	public void speak() {
		System.out.println("小鸟喳喳叫");	
	}
	
	public void fly() {
		System.out.println("小鸟在天上飞");	
	}
	public void eat() {
		System.out.println("小鸟吃果子");		
	}
}
