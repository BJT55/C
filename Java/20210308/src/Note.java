//    栈：
//    1.顺序表：使用尾插操作表示“入栈”，使用尾删操作表示“出栈”，使用根据下标后去元素的操作表示“取栈顶元素”
//    2.链表：使用头插操作表示“入栈”，使用头删操作表示“出栈”，直接取到头结点就是“取栈顶元素”
//    栈的核心操作：
//    a.入栈：把元素放入栈里面
//    b.出栈：把最后进来的元素给删掉
//    c.取栈顶元素：获取到最后一个进来的元素的结果

//    队列：
//    1.顺序表：可以使用特殊的方式实现
//    2.链表：使用尾插表示“入队列”，使用头删表示“出队列”，直接获取头结点，表示“去队列首元素”

//    所谓的栈和队列，就是在顺序表和链表上套了一层壳，栈和队列的基本操作其实都是容纳到顺序表和链表的基本操作之中
