package Animals;
class People implements Animal {

	public void eat() {
		System.out.println("人是杂食动物");
	}

	public void sleap() {
		System.out.println("人能睡觉");
	}

	public void think() {
		System.out.println("人可以进行复杂的思考");
	}

	public void speak() {
		System.out.println("人可以说话");
	}

	public void fly() {
		System.out.println("人不能飞");
	}
}
